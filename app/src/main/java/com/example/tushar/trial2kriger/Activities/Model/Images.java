package com.example.tushar.trial2kriger.Activities.Model;

public class Images {
    public String image;
    public String userid;

    public Images() {
    }

    public Images(String image,  String userid) {
        this.image = image;
        this.userid = userid;
    }


    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }
}



